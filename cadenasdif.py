## Vamos a crear una función que reciba dos cadenas de textos, nos retorne dos cadenas pero que en la primera no haya 
# los caracteres de la segunda y viceversa 

def cadenas(cadena_1 = 'hola', cadena_2 = 'vete'):
    salida_1 = cadena_1
    salida_2 = cadena_2
    print(salida_1, ' ', salida_2)

cadenas('aja', 'no sé')

